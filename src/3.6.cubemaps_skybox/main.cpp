		setKeyCallback(cameraCallback);
		glfwSetCursorPosCallback(window, mouse_callback);
		glfwSetMouseButtonCallback(window, mouse_button_callback);

		glEnable(GL_DEPTH_TEST);

		shaderSky = new Shader("skybox.vs", "skybox.fs");
		shaderSky->use();

		glm::mat4 model;
		shaderSky->setMatrixVec4("model", glm::value_ptr(model));

		glm::mat4 view;
		view = camera->GetViewMatrix();
		shaderSky->setMatrixVec4("view", glm::value_ptr(view));

		//投影矩阵
		glm::mat4 projection;
		projection = glm::perspective(45.0f, (float)width / height, 0.1f, 100.0f);
		shaderSky->setMatrixVec4("projection", glm::value_ptr(projection));

		shaderSky->setInt("skybox", 0);

		cubeTexture = TextureFromFile("marble.jpg", "E:/OpenGLTutorial/resources/textures");
		shaderCube = new Shader("stencil_testing.vs", "stencil_testing.fs");
		shaderCube->use();
		shaderCube->setMatrixVec4("model", glm::value_ptr(model));
		shaderCube->setMatrixVec4("view", glm::value_ptr(view));
		shaderCube->setMatrixVec4("projection", glm::value_ptr(projection));
		shaderCube->setInt("skybox", 0);
		//shaderCube->setInt("texture1", 0);
